def fill_in_cbeta_for_atom3_all(coords):
    # coords shape: b n 3 3, only n, ca, c atoms
    b = coords[..., 1, :] - coords[..., 0, :] # shape: b n 3
    c = coords[..., 2, :] - coords[..., 1, :] # shape: b n 3
    a = torch.cross(b, c, dim=-1) # shape: b n 3
    cbeta = -0.58273431 * a + 0.56802827 * b - 0.54067466 * c + coords[..., 1, :]
    # shape: b n 3
    new_coords = torch.clone(coords)    
    new_coords = torch.cat([new_coords, cbeta.unsqueeze(2)], dim=2)
    
    return new_coords # shape: b n 4 3



